-- ---------- CLSDataStore ----------

-- | The data store maintains and syncs your app's contexts.
-- 
-- Phantom type for @CLSDataStore@.
data CLSDataStore

instance IsObjCObject (Id CLSDataStore) where
  toRawId = idToRawId
  withObjCPtr = withIdObjCPtr
  staticClass _ = getRequiredClass "CLSDataStore"

class IsNSObject a => IsCLSDataStore a where
  toCLSDataStore :: a -> Id CLSDataStore

